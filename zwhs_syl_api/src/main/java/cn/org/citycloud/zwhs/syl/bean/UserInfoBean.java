package cn.org.citycloud.zwhs.syl.bean;

import javax.validation.constraints.NotNull;

import org.hibernate.validator.constraints.NotEmpty;

public class UserInfoBean {
	
	@NotEmpty
	private String user_name;
	
	private String user_pwd;
	
	private String user_truename;
	
	private Integer user_phone;
	
	private Integer role_id;

	public String getUser_name() {
		return user_name;
	}

	public void setUser_name(String user_name) {
		this.user_name = user_name;
	}

	public String getUser_pwd() {
		return user_pwd;
	}

	public void setUser_pwd(String user_pwd) {
		this.user_pwd = user_pwd;
	}

	public String getUser_truename() {
		return user_truename;
	}

	public void setUser_truename(String user_truename) {
		this.user_truename = user_truename;
	}

	public Integer getUser_phone() {
		return user_phone;
	}

	public void setUser_phone(Integer user_phone) {
		this.user_phone = user_phone;
	}

	public Integer getRole_id() {
		return role_id;
	}

	public void setRole_id(Integer role_id) {
		this.role_id = role_id;
	}
	
	
}
