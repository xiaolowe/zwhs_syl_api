package cn.org.citycloud.zwhs.syl.bean;

import java.util.Date;

import javax.validation.constraints.NotNull;

import org.hibernate.validator.constraints.NotEmpty;

public class StoreCouponBean {
	
	@NotEmpty
	private String coupon_name;
	
	@NotNull
	private Double coupon_money;
	
	@NotNull
	private Double coupon_condition;
	
	@NotNull
	private Integer coupon_number;
	
	@NotNull
	private Integer coupon_limit;
	
	@NotEmpty
	private String coupon_desc;
	
	private Date effective_time;
	
	private Date expiration_time;
	
	private String couponSymbol = ">=";
	

	public String getCoupon_name() {
		return coupon_name;
	}

	public void setCoupon_name(String coupon_name) {
		this.coupon_name = coupon_name;
	}

	public Double getCoupon_money() {
		return coupon_money;
	}

	public void setCoupon_money(Double coupon_money) {
		this.coupon_money = coupon_money;
	}

	public Double getCoupon_condition() {
		return coupon_condition;
	}

	public void setCoupon_condition(Double coupon_condition) {
		this.coupon_condition = coupon_condition;
	}


	public Integer getCoupon_number() {
		return coupon_number;
	}

	public void setCoupon_number(Integer coupon_number) {
		this.coupon_number = coupon_number;
	}

	public Integer getCoupon_limit() {
		return coupon_limit;
	}

	public void setCoupon_limit(Integer coupon_limit) {
		this.coupon_limit = coupon_limit;
	}

	public String getCoupon_desc() {
		return coupon_desc;
	}

	public void setCoupon_desc(String coupon_desc) {
		this.coupon_desc = coupon_desc;
	}

	public Date getEffective_time() {
		return effective_time;
	}

	public void setEffective_time(Date effective_time) {
		this.effective_time = effective_time;
	}

	public Date getExpiration_time() {
		return expiration_time;
	}

	public void setExpiration_time(Date expiration_time) {
		this.expiration_time = expiration_time;
	}

	public String getCouponSymbol() {
		return couponSymbol;
	}

	public void setCouponSymbol(String couponSymbol) {
		this.couponSymbol = couponSymbol;
	}

}
