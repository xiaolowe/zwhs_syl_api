package cn.org.citycloud.zwhs.syl.bean;


public class ServiceProviderSearchBean {
	
	private Integer pageNo = 1;
	
	private Integer pageSize = 10;
	
	private String companyName;
	
	private Integer provider_state;


	public Integer getPageNo() {
		return pageNo;
	}


	public void setPageNo(Integer pageNo) {
		this.pageNo = pageNo;
	}


	public Integer getPageSize() {
		return pageSize;
	}


	public void setPageSize(Integer pageSize) {
		this.pageSize = pageSize;
	}


	public String getCompanyName() {
		return companyName;
	}


	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}


	public Integer getProvider_state() {
		return provider_state;
	}


	public void setProvider_state(Integer provider_state) {
		this.provider_state = provider_state;
	}
	
	
}
